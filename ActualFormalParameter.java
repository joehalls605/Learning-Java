/*
Actual vs Formal Parameter

Formal Parameters:

Parameters defined in the method signature.
They act as placeholders for the values that will be passed when the method is called.
Example:
*/

void greet(String name) {
    System.out.println("Hello, " + name);
}

/*
Actual Parameters (Arguments):

The real values passed to the method when it is called.
Example:
*/

greet("Alice"); // "Alice" is the actual parameter

